#!/usr/bin/env node
// AUTO release-notes generator for the "what's new" popup.
//
// Writes src/lib/releases.generated.ts from recent git commit subjects so the
// popup needs zero manual authoring. Wired to npm `prebuild`, so it runs right
// before `next build` (locally and on Vercel git-connected builds).
//
// Resolution order:
//   1. Local git history (works locally and on Vercel git-connected builds).
//   2. Vercel git env (VERCEL_GIT_COMMIT_SHA/_MESSAGE) when only HEAD is known.
//   3. No source available (e.g. a Vercel CLI build with no .git): keep the
//      committed file as-is so a locally generated list still ships.

import { execSync } from "node:child_process";
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUT = join(root, "src/lib/releases.generated.ts");
const MAX = 30; // commits to remember (the dedup window)
const SEP = "\u001f";

// Commit subjects we never surface as a "feature".
const NOISE = /^(merge|wip|fixup|squash|revert|bump|release|version|typo|lint|format|chore|ci|build|deploy)\b/i;

function clean(subject) {
  return subject
    .replace(/^v\d+(\.\d+)*[:.]?\s*/i, "") // "v74: ", "v1.2 "
    .replace(/^(feat|fix|refactor|docs|style|perf|test)(\(.+?\))?:\s*/i, "") // conventional prefixes
    .trim();
}

function fromGit() {
  const raw = execSync(`git log -n ${MAX} --no-merges --pretty=format:%h${SEP}%s`, {
    encoding: "utf8",
    stdio: ["ignore", "pipe", "ignore"],
  });
  return raw
    .split("\n")
    .map((line) => {
      const [sha, ...rest] = line.split(SEP);
      return { sha: (sha || "").trim(), subject: rest.join(SEP).trim() };
    })
    .filter((c) => c.sha && c.subject);
}

function fromVercelEnv() {
  const sha = process.env.VERCEL_GIT_COMMIT_SHA;
  if (!sha) return [];
  const subject = (process.env.VERCEL_GIT_COMMIT_MESSAGE || "").split("\n")[0] || "Updates and improvements";
  return [{ sha: sha.slice(0, 7), subject }];
}

// The list already committed to the repo. On a shallow Vercel clone `git log`
// may return only HEAD, so we merge fresh commits on top of this rather than
// clobbering a richer history.
function fromExisting() {
  if (!existsSync(OUT)) return [];
  try {
    const text = readFileSync(OUT, "utf8");
    const m = text.match(/=\s*(\[[\s\S]*\]);\s*$/);
    return m ? JSON.parse(m[1]) : [];
  } catch {
    return [];
  }
}

let fresh = [];
try {
  fresh = fromGit();
} catch {
  fresh = [];
}
if (fresh.length === 0) fresh = fromVercelEnv();

// Clean + drop noise from the fresh commits, then merge on top of the existing
// committed list (newest first, deduped by sha). This keeps a deep history even
// when the build clone is shallow, and always includes the commit being built.
const freshClean = fresh
  .map((c) => ({ sha: c.sha, subject: clean(c.subject) }))
  .filter((c) => c.subject && !NOISE.test(c.subject));

const seen = new Set();
let list = [...freshClean, ...fromExisting()]
  .filter((c) => c && c.sha && c.subject && !seen.has(c.sha) && seen.add(c.sha))
  .slice(0, MAX);

if (list.length === 0) list = [{ sha: "dev", subject: "Updates and improvements" }];

const buildDate = new Date().toISOString().slice(0, 10);
const body =
  "// AUTO-GENERATED by scripts/gen-releases.mjs — do not edit by hand.\n" +
  "// Regenerated at build time from recent git commit subjects.\n" +
  `export const buildDate = ${JSON.stringify(buildDate)};\n` +
  `export const commits: { sha: string; subject: string }[] = ${JSON.stringify(list, null, 2)};\n`;
writeFileSync(OUT, body);
console.log(`[gen-releases] wrote ${list.length} commit(s) -> src/lib/releases.generated.ts`);
